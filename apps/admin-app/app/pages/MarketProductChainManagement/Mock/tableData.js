export const getTableData = (pageType, activeTab) => {
  if (pageType === PAGE_TYPES.PRODUCT && !activeTab) {
    return productListMockData.data;
  }
  const data = pageType === PAGE_TYPES.PRODUCT ? productPageData : warehousePageData;
  return data[activeTab]?.data || [];
};

export const getTabCounts = pageType => {
  const data = pageType === PAGE_TYPES.PRODUCT ? productPageData : warehousePageData;
  return Object.entries(data).reduce((acc, [key, value]) => {
    acc[key] = value.count;
    return acc;
  }, {});
};

